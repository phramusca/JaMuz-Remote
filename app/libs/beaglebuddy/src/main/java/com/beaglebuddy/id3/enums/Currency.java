   // data members
   private String code;
   private String name;

   /**
    * constructor.
    * @param name   name of the currency.
    */
   private Currency(String name)
   {
      this.code = super.toString().toLowerCase();
      this.name = name;
   }

   /**
    * gets the ISO-4217 currency code.
    * @return the ISO-4217 currency code.
    */
   public String getCode()
   {
      return code;
   }

   /**
    * gets the three character ISO-4217 currency code as an array of bytes, using 1 byte for each character.
    * @return the three character ISO-4217 currency code as an array of bytes.
    */
   public byte[] getCodeBytes()
   {
      return code.getBytes(Encoding.ISO_8859_1.getCharacterSet());
   }

   /**
    * gets the name of the ISO-4217 currency.
    * @return the name of the currency.
    */
   public String getName()
   {
      return name;
   }

   /**
    * converts a three character String currency code value to its corresponding ISO-4217 currency enum.
    * @return an ISO-4217 currency enum corresponding to the string value.
    * @param code  string value of the currency code to be converted to an ISO-4217 currency enum.
    * @throws IllegalArgumentException   if the string currency code is not a valid ISO-4217 currency code.
    */
   public static Currency getCurrency(String code) throws IllegalArgumentException
   {
      if (code == null || code.length() != 3)
         throw new IllegalArgumentException("Invalid currency " + code + ".");

      for (Currency c : Currency.values())
         if (code.equalsIgnoreCase(c.getCode()))
            return c;
      throw new IllegalArgumentException("Invalid currency " + code + ".");
   }

   /**
    * gets a string representation of the ISO-4217 currency enum.
    * @return a string representation of the ISO-4217 currency enum.
    */
   public String toString()
   {
      return code + " - " + name;
   }
